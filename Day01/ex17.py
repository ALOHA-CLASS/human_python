# 비교 연산자
a = 15
print('{} > 10 : {}'.format(a, a > 10))     # True
print('{} < 10 : {}'.format(a, a < 10))     # False
print('{} >= 10 : {}'.format(a, a >= 10))   # True
print('{} <= 10 : {}'.format(a, a <= 10))   # False
print('{} == 10 : {}'.format(a, a == 10))   # False
print('{} != 10 : {}'.format(a, a != 10))   # True