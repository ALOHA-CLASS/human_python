# 컬렉션 사용하기

# list      - 리스트
# 빈 리스트 생성
li = list()
print(li)

# 리스트 생성 및 초기화
li = list([1,2,3,4,5])
print(li)

# 리스트 초기화
li = [1,2,3,4,5]
print(li)




# tuple     - 튜플
# 빈 튜플 생성
tu = tuple()
print(tu)

# 튜플 생성 및 초기화
tu = tuple([1,2,3,4,5])
print(tu)

# 튜플 초기화
tu = (1,2,3,4,5)
print(tu)

# set       - 세트
# 빈 세트 생성
s = set()
print(s)

# 세트 생성 및 초기화
s = set([1,2,3,4,5])
print(s)

# 세트 초기화
s = {1,2,3,4,5}
print(s)



# dict      - 딕셔너리
# 딕셔너리 생성
d = dict()
print(d)

# 딕셔너리 생성 및 초기화
d = dict({"a" : 1, "b" : 2, "c" : 3})
print(d)

# 딕셔너리 초기화
d = {"a" : 1, "b" : 2, "c" : 3}
print(d)